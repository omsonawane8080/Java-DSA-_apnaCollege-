public class DataTypesExample {
    public static void main(String[] args) {
        // Integer (int)
        int age = 25;
        int year = 2025;
        int studentCount = 120;

        // Floating point numbers
        float temperature = 36.5f; // 'f' is needed for float values
        double pi = 3.14159265359;
        double accountBalance = 10500.75;

        // Character (char)
        char grade = 'A';
        char firstLetter = 'O';

        // Boolean (boolean)
        boolean isStudent = true;
        boolean hasLicense = false;

        // String (String)
        String name = "Om Sonawane";
        String city = "Pune";
        String country = "India";

        // Byte (byte)
        byte smallNumber = 100; // Range: -128 to 127

        // Short (short)
        short totalMarks = 32000; // Range: -32,768 to 32,767

        // Long (long)
        long worldPopulation = 8000000000L;
        long distanceToMoon = 384400000L; // in meters
        
        // Output some values to check
        System.out.println("Name: " + name);
        System.out.println("Age: " + age);
        System.out.println("Temperature: " + temperature);
        System.out.println("Pi Value: " + pi);
        System.out.println("World Population: " + worldPopulation);
    }
}
